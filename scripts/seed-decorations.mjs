async function main() {
  console.log('Seeding decoration techniques...')

  let created = 0
  let updated = 0

  for (const technique of decorationTechniques) {
    try {
      await prisma.decorationTechnique.upsert({
        where: { sku: technique.sku },
        update: {
          name: technique.name,
          category: technique.category,
          subcategory: technique.subcategory,
          skillLevel: technique.skillLevel,
          description: technique.description,
          unit: technique.unit,
          defaultCostPerUnit: technique.defaultCostPerUnit,
          laborMinutes: technique.laborMinutes,
          wasteFactorPercent: technique.wasteFactorPercent,
          materialGrade: technique.materialGrade,
          toolsRequired: technique.toolsRequired,
          imageReference: technique.imageReference,
        },
        create: technique,
      })
      console.log(`  ✓ ${technique.sku} - ${technique.name}`)
      created++
    } catch (error) {
      console.error(`  ✗ Failed: ${technique.sku}:`, error.message)
    }
  }

  console.log(`\nDone! Imported ${created} decoration techniques.`)

  // Show summary by category
  const categories = await prisma.decorationTechnique.groupBy({
    by: ['category'],
    _count: { id: true },
  })

  console.log('\nBy category:')
  for (const cat of categories) {
    console.log(`  ${cat.category}: ${cat._count.id}`)
  }
}

main()
  .catch((e) => {
    console.error(e)
    process.exit(1)
  })
  .finally(async () => {
    await prisma.$disconnect()
  })
